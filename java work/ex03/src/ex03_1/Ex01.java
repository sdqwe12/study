package ex03_1;

public class Ex01 {
	public static void main(String[] args) {
		System.out.println(3%2);
		//%는 나머지 3을 2로 나누면 나머지는 1
		System.out.println(2<<2);
		// <<부호 두개쓰면 비트연산자 한개쓰면 비교 / 비트는 잘안씀
		System.out.println(10>3);
		System.out.println(10<=3);
		
		System.out.println(10==10); // == 같냐
		System.out.println(10!=10); // != 다르냐
		
		int a = 10;
		int b = 20;
		System.out.println(a==b);
		System.out.println(a!=b);

	}

}
